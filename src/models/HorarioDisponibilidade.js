class HorarioDisponibilidade {

    constructor(id, turno, inicioIntervalo, fimIntervalo, inicioHorario, fimHorario, horariosAula){
        this.id = id;
        this.turno = turno;
        this.inicioIntervalo = inicioIntervalo;
        this.fimIntervalo = fimIntervalo;
        this.inicioHorario = inicioHorario;
        this.fimHorario = fimHorario;
        this.horariosAula = horariosAula;
    }

}